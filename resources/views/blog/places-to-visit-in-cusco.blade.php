                    <div id="hatunrumiyoc" style="padding-top:70px">
                        <h5> 5. Hatun Rumiyoc Street and 12 Angle Stone </h5>
                        <p>Hatun Rumiyoq is a stone wall (green diorite) located on the street of the same name. It must
                            have been part of the palace attributed to Inca Roca. This covers almost all of Hatun
                            Rumiyoc Street; street known worldwide because it houses the “stone of the twelve angles”,
                            famous for the perfect assembly of its 12 corners and its sides, with the stones that
                            surround it.
                            <br> This wall has overlapping colonial and republican structures, and is currently occupied
                            by the Archbishop's Palace and the Museum of Religious Art of Cusco. In this place the
                            architects had to build a kind of platform, to level the terrain, since the steep slope that
                            leads to the traditional neighborhood of San Blas begins here.
                            <br> It is located about 200 m from the Plaza de Armas of the City of Cusco, in Peru; It is
                            reached in a northeast direction by the side of the Iglesia del Triunfo; From here walk
                            straight and you will reach Hatun Rumiyoc Pedestrian Street.
