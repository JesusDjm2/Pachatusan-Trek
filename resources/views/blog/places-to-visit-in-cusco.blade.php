                        <h5> 2. San Blas neighborhood </h5>
                        <p>San Blas, located in the historic center, is the most beautiful and charming neighborhood to
                            see in Cusco. Strolling through the narrow and steep cobblestone streets, seeing the old
                            colonial houses or looking for some original souvenir in one of the shops and craft
                            workshops, are some of the best things to do in Cuzco.
                            <br> Among its most interesting places is the Church of San Blas, the oldest in the city
                            built on an Inca temple that is worth going inside to see its fantastic carved wooden
                            pulpit.
                            <br> Also in this neighborhood, being in the upper part of the city, you will have good
                            views of the city. It is recommended to finish a route through Cuzco in a day returning to
                            this bohemian neighborhood at night to have a pisco sour in one of its picturesque places
