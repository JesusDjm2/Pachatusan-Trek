                    <div id="tambomachay" style="padding-top:70px"><h5> 7. Tambomachay, Pukapukara, Qenqo and Cristo Blanco </h5>
                    <p>In addition to Sacsayhuamán, there are three other Inca archaeological sites that deserve to
                        be among the essential places to visit in Cusco. The first is Tambomachay, dedicated to the
                        cult of water and located 8 kilometers from Cusco, which highlights a beautiful natural
                        environment where an Inca hydraulic system consisting of sources, channels and aqueducts was
                        located.
                        <br> A 5-minute walk from Tambomachay is Pukapukara, known as the Red Fort. In these ruins
                        you can see the different rooms and walls of the old fortress, which could be used as a
                        hunting lodge, guard post or a stopping place for the royal procession.
                        <br> Following the road in the direction of Sacsayhuamán you will arrive, in an hour on
                        foot, to Qenqo, a religious complex where you can see the remains of various Inca buildings
                        such as an amphitheater, two columns with astronomical functions, a canal and an underground
                        room . Qenqo is located one kilometer from Sacsayhuamán and also very close to the Cristo
                        Blanco, an 8-meter statue from which you can take the way down to the city, with fantastic
                        views of the city of Cusco.
                        <br> To save money we advise you to book the specific tourist ticket for the Cusco area that
                        includes all these ruins and the tourist attractions of the city. Keep in mind that although
                        it is recommended to do this walking tour of the landscapes, you can take a combi that
                        brings you closer to this site.
