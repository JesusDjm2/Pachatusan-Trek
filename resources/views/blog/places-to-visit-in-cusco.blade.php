                    <div id="sanpedro" style="padding-top:70px">
                        <h5> 6. San Pedro Market </h5>
                        <p>Another of the places to see in Cuzco is the Mercado de San Pedro, the oldest and best known
                            in the city. This market, also called Central Market, was built by the famous Gustav Eiffel,
                            author of the Eiffel Tower and inside you will see stalls of all kinds of typical products
                            of Peru such as passion fruit, pineapple, corn, cassava, Andean potatoes and others more
                            common in all markets. It is also a good place to buy handicrafts and try typical local
                            dishes at one of the food stalls. The Central Market of Cusco is located 5 blocks from the
                            Plaza de Armas of Cusco in a western direction near the San Pedro train station.
                            <br> The Central Market of Cusco houses a large number of food stalls, with entire sections,
                            dedicated exclusively to fruit juices and juices, snacks and prepared meals. If the weather
                            is cold, one of the best options is head broth (from lamb), but if, as usual, the sun
                            shines, you can enjoy a delicious ceviche (raw fish marinated in lemon juice).
                            <br> Hidden among the corridors of the Mercado de San Pedro (in the fruit section) you can
                            find stalls with exotic products brought from the Amazon, tinctures of herbs, and
                            sebum-based ointments from certain animals. Here you can buy coca leaves and prepare your
                            own infusion; Coca tea is one of the best natural remedies to combat altitude sickness.
                            There is a row dedicated to chocolate, here you can find a mixture of delicious chocolate,
                            coca leaves and mint (very delicious). It is not unusual that people who come from nearby
                            provinces come in typical costumes, carrying their babies on their backs in typical
                            blankets, from which it seems that they are going to fall, but you will see that this is not
                            the case.
