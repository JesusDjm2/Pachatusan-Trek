                   <div id="machupicchu" style="padding-top:70px"> <h5> 9. Machu Picchu </h5>
                    <p>
                        Machu Picchu, the lost city of the Incas, located just over 100 kilometers from Cusco, is
                        considered one of the seven wonders of the world as well as one of the most important places
                        to visit in Cusco. This ancient Inca city, which was hidden between the mountains and the
                        vegetation until the explorer Hiram Bingham discovered it in 1911, stands out for its
                        15th-century ruins such as the Intihuatana stone, the Temple of the Sun, the Main Temple,
                        the Temple of the Condor and the Three Windows Room.
                        <br> To get to know the history of this ancient city better and not miss any interesting
                        place, it is advisable to do part of the visit with a guide. To visit the city of Machu
                        Picchu and climb the Huaynapicchu you must reserve the entrance several months in advance,
                        as you have a limited number of daily visitors. To get to Machu Picchu by public transport,
                        you can take a train in Cuzco or Ollantaytambo to Aguas Calientes, where buses leave every
                        hour to take you to the entrance of the archaeological site.
                        <br> Another option is to book this day trip that includes transportation, hotel pickup,
                        entrance, and a guide in Spanish during your visit to Machu Picchu. That said, we have to
                        highlight that the most magical way to get to Machu Picchu is by walking the 40 kilometers
                        of the Inca Trail, which have made it one of the best things to do in Peru. This 4-day
                        excursion through paths with ancient stone steps, valleys and mountains with dream
                        landscapes, will take you to this wonder of the world. You can shorten part of the journey
