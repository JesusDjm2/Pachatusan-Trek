                    <div id="sacsayhuaman" style="padding-top:70px">
                        <h5> 4. Sacsayhuamán </h5>
                        <p>
                            Located about 40 minutes walk north of the historic center, there are the ruins of the
                            Sacsayhuamán fortress, another of the essential places to visit in Cusco. This complex began
                            to be built in the 15th century by order of Pachacutec, one of the most important Incas that
                            the Empire had. And although currently only a part of the old Sacsayhuamán is preserved, you
                            can still see the incredible walls of large stone blocks forming 3 overlapping platforms.
                            Keep in mind that in the complex you can walk through the different platforms using the
                            stairs and doors made of stone such as the fantastic Puerta del Sol and on various stones of
                            the walls, which reach 400 meters in length, see various images made by the Incas .
                            <br> If you have time, we recommend doing a route that can take you one morning, first
                            visiting the Inca ruins of Tambomachay, Pukapukara, Qenqo and ending with Sacsayhuamán. To
                            do this route you can take a combi in the Plaza de Armas until you reach the last site and
                            walk down the hill visiting the rest of the temples.
                            <br> Visiting hours: every day from 07:00 to 18:00
                            <br><a href="#" id="modal-p" data-toggle="modal" data-target="#exampleModal"
                                data-whatever="@mdo"> Book this private tour. </a>
