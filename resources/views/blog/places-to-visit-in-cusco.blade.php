                    <div id="places" style="padding-top:70px">
                        <p> This list of <strong> the best places to visit in Cusco </strong> will help you prepare a
                            route through one of the most historic and beautiful cities in Latin America, which we are
                            sure will not disappoint. Located on a slope of the Andes mountain range, Cusco was the
                            capital of the Inca Empire and one of the most important cities in the Spanish colonial era.
                            Declared a World Heritage Site, it still preserves remains of Inca temples and constructions
                            in the foundations of numerous colonial buildings in which this mixture of two cultures can
                            be observed while walking through its cobbled streets and squares, visiting the most
                            important temples and churches, as well as when try its delicious gastronomy accompanied by
                            a pisco sour. In addition, the city is perfect as a base to visit some of the great jewels
                            of Peru such as the Sacred Valley, the Mountain of the 7 colors and Machu Picchu. </p>
