                    <div id="sacredvalley" style="padding-top:70px"><h5> 8. The Sacred Valley of the Incas </h5>
                    <p> The magical Sacred Valley of the Incas, located 15 kilometers from Cusco, is one of the most
                        impressive places to visit in Peru. In this Andes valley that passes several rivers, you
                        will find archaeological sites of the ancient Incas, indigenous and colonial towns and
                        landscapes that will leave you speechless.
                        <br> This valley was highly appreciated by the Incas for its good conditions for agriculture
                        in which, taking advantage of the geography, they created a system of terraces to cultivate
                        their precious corn. Among the essential places to see in the Sacred Valley are:
                        <br> Pisac: The impressive Inca ruins situated on a hill, the cultivation terraces and the
                        handicraft market are enough reasons to visit Pisac. Ollantaytambo: This is one of the
                        masterpieces of Inca architecture in which an enormous complex of several floors built on
                        two mountains stands out, which had defensive, religious and administrative functions.
                        Moray: In this place you can see perfect circular terraces that would have been used by the
                        Incas to do experiments with different crops. Salineras de Maras: Thousands of small pools
                        filled with water form this old salt mine that will leave a postcard image on your retina.
                        Chinchero: This indigenous town is one of the most beautiful in the Sacred Valley. Do not
                        miss the square located on what was an Inca palace and the stalls to buy crafts that mount
                        in the surroundings.
                        <br> To save money we advise you to book the specific tourist ticket for the Cusco area that
                        includes all these ruins and the tourist attractions of the city. Keep in mind that although
                        it is recommended to do this walking tour of the landscapes, you can take a combi that
                        brings you closer to this site.
                        <br>
                    </p>
