                    <div id="plaza" style="padding-top:70px">
                        <h5>1. Plaza de Armas</h5>
                        <p>
                            The Plaza de Armas is the heart of the historic center and one of the most important places
                            to visit in Cusco. Formerly, this square was a swamp that the Incas dried to make it the
                            center of administration for the entire Empire, but with the arrival of the Spanish,
                            numerous buildings and churches were built around the square, on top of the Inca temples and
                            palaces. Today the square is full of tourist restaurants and large chains such as McDonald’s
                            or Starbucks, travel agencies and souvenir shops, which for us have taken away the charm of
                            past times, although it is still a must in the city.
                            <br> Two of the most interesting places that you can visit in the square are the Cathedral
                            of Cusco and the Church of the Company of Jesus. The Cathedral with a mix of Baroque, Gothic
                            and Plateresque styles has several wonders inside such as the chapels, the main altar and
                            the sacristy. Next to the Cathedral, there is the Iglesia de la Compañía de Jesús, one of
                            the most beautiful baroque churches in Peru where, in addition to observing its impressive
                            façade, it is worth entering to see the main altar, the 4 cedar altars and the golden
                            pulpit.
