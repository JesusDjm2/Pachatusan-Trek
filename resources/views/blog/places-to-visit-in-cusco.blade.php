                    <div id="qoricancha" style="padding-top:70px">
                        <h5> 3. Qorikancha and the Convent of Santo Domingo </h5>
                        <p>Qorikancha was a temple built by the Incas dedicated to the Sun god that over time expanded
                            and beautified until it became the most important place of worship in the Inca Empire. One
                            of the most outstanding elements that are preserved from the temple are the large blocks of
                            solid rock that were joined without any mortar, to form large walls. These walls that were
                            covered with gold foil delimited several temples within Qorikancha such as the Sun, the Moon
                            and the Stars.
                            <br> With the arrival of the Spanish, the Convent of Santo Domingo was built on top of
                            Qorikancha, destroying the upper part of the Inca temple.
                            <br> Visiting hours: Monday to Saturday from 8:30 a.m. to 5:30 p.m. Sunday from 2pm to 5pm.
