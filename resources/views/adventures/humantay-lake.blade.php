                        <h2 id="titulo1">HUMANTAY LAKE TOUR - HUMANTAY TREK (Full Day)</h2>
                        <div id="separadordjm2"></div>
                        <p style="color: #0c8178">Reserve 30 days in advance
                    </center>
                    <h5>Overview</h5>
                    <p>
                        With this program we will visit one of the new tourist circuits of Cusco. The <strong>"Humantay
                            Lagoon"</strong>
                        is a natural attraction located in the village of<strong> Soraypampa,</strong> formerly only
                        visited by those who
                        ventured to make the trek to the Salkantay, but now it has become one of the most visited places
                        by travelers. Visiting the Humantay Lagoon will not take you more than 1 day, the walk is easy
                        if you have previously acclimatized yourself in Cusco and the reward for this effort will be the
                        wonderful landscape that is observed in this turquoise lagoon. <br><br>

                        <img src="{{ asset('img/galeria/tour-humantay-laguna.webp') }}" alt="humantay lake"
                    width="100%" loading="lazy"><br><br>
                    </p>
                    <h5>ITINERARY:</h5>
                    <p><strong>CUSCO - CHIQUISCA</strong></p>
                    <p>The trip starts at your hotel in Cusco; our transfer will visit you between 04:30 and 05:00 in
                        the morning. <br> We will make a trip of two and a half hours by car to the town of Mollepata, where
                        we can have breakfast and stock up on the latest provisions. <br><br>After breakfast we will continue
                        our trip for an hour and a half by car; the road we will take is a dirt (path) to reach the
                        community of Soraypampa which is located at an altitude of 3900 meters above sea level.<br> During
                        the trip we will see beautiful landscapes and the impressive snowy "Salkantay". From Soraypampa
                        we will start a 2 km walk. The way is ascending and it will take us about an hour and a half to
                        two.<br><br> While we do the walk we will see a panorama of snow-capped mountains, impressive natural
                        landscapes, and a great diversity of flora and fauna until we reach the "Laguna de Humantay". It
                        is located at an altitude of 4200 meters above sea level.<br><br>

                        <img src="{{ asset('img/galeria/tour-humantay-turista.webp') }}" alt="humantay lake tour"
                    width="100%" loading="lazy"><br><br>

                        The "Humantay Lagoon" is turquoise due to the mineral found in the bottom; the lagoon is fed by
                        the glaciers of the mountain and is considered a jewel of nature. In addition to delighting in
                        the Humantay Lagoon we can enjoy the impressive views of the mountains that surround it. We will
                        descend walking until we reach the community of Soraypampa where we can enjoy a buffet lunch.
                        After lunch we will start the return trip to the city of Cusco. Our tour ends at approximately
                        6:30 in the afternoon. Our last stop will be near the Plaza de Armas of Cusco, thus ending our
                        hike to the Humantay lagoon for 1 day.</p>

                        <img src="{{ asset('img/galeria/humantay-lake.webp') }}" alt="humantay lake tour"
                        width="100%" loading="lazy"><br><br>

                    <h5>We Suggest</h5>
                    <p>
                        Almost all the way is asphalted; However, we will continue along precipitous dirt roads, so we
                        recommend going as light as possible and not forgetting water, torch, Trekking sticks, if
                        possible, and plenty of shelters.
                    </p>
